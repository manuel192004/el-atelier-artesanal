export const collectionOrder = ['anillos', 'aretes', 'cadenas', 'pulseras'];

export function createReference(slug, id) {
  return `${slug.slice(0, 3).toUpperCase()}-${String(id).padStart(3, '0')}`;
}

function inferMetal(slug, item) {
  if (slug === 'anillos') {
    if (item.category === 'Oro Blanco') return 'Oro blanco';
    if (item.category === 'Bicolor') return 'Bicolor';
    return 'Oro amarillo';
  }

  return 'Oro amarillo';
}

function inferType(slug, itemName) {
  const name = itemName.toLowerCase();

  if (slug === 'anillos') {
    if (name.includes('solitario')) return 'Solitario';
    if (name.includes('halo')) return 'Halo';
    if (name.includes('cluster')) return 'Cluster';
    if (name.includes('media eternidad')) return 'Media eternidad';
    if (name.includes('eternidad')) return 'Eternidad';
    if (name.includes('banda')) return 'Banda';
    if (name.includes('bisel')) return 'Bisel';
  }

  if (slug === 'aretes') {
    if (name.includes('topos')) return 'Topos';
    if (name.includes('argollas')) return 'Argollas';
    if (name.includes('cuff')) return 'Cuff';
    if (name.includes('perla')) return 'Perla';
    if (name.includes('colgante') || name.includes('cadenas') || name.includes('gota')) return 'Colgantes';
    return 'Aretes de autor';
  }

  if (slug === 'cadenas') {
    if (name.includes('brazalete')) return 'Brazalete';
    if (name.includes('bolitas')) return 'Bolitas';
    if (name.includes('cordon')) return 'Cordon';
    if (name.includes('mixta')) return 'Mixta';
    if (name.includes('cadena')) return 'Cadena';
    return 'Eslabon';
  }

  if (name.includes('rigid')) return 'Rigida';
  if (name.includes('cordon')) return 'Cordon';
  if (name.includes('esferas')) return 'Esferas';
  if (name.includes('cadena')) return 'Cadena';
  return 'Eslabon';
}

function inferStyle(slug, itemName) {
  const name = itemName.toLowerCase();

  if (name.includes('pave') || name.includes('brillo') || name.includes('halo')) return 'Brillante';
  if (name.includes('floral') || name.includes('perla')) return 'Romantico';
  if (name.includes('trenzad') || name.includes('cordon')) return 'Texturizado';
  if (name.includes('rectangular') || name.includes('cuff') || name.includes('rigid')) return 'Statement';
  if (name.includes('delicad') || name.includes('topos') || name.includes('solitario')) return 'Delicado';

  if (slug === 'anillos') return 'Clasico';
  if (slug === 'aretes') return 'Ligero';
  if (slug === 'cadenas') return 'Versatil';
  return 'Clasico';
}

function inferProtagonism(slug, itemName) {
  const name = itemName.toLowerCase();

  if (name.includes('halo') || name.includes('cluster') || name.includes('ancha') || name.includes('doble') || name.includes('gruesa')) {
    return 'Alto';
  }

  if (name.includes('delicad') || name.includes('topos') || name.includes('solitario') || name.includes('perla')) {
    return 'Bajo';
  }

  if (slug === 'aretes' && name.includes('argollas lisas')) return 'Medio';
  return 'Medio';
}

function inferOccasions(slug, itemName) {
  const name = itemName.toLowerCase();

  if (slug === 'anillos') {
    if (name.includes('solitario') || name.includes('halo')) return ['Compromiso', 'Aniversario'];
    if (name.includes('eternidad')) return ['Aniversario', 'Regalo especial'];
    return ['Uso diario', 'Regalo especial'];
  }

  if (slug === 'aretes') {
    if (name.includes('perla') || name.includes('gota') || name.includes('flores')) return ['Evento', 'Regalo especial'];
    if (name.includes('topos')) return ['Uso diario', 'Regalo especial'];
    return ['Uso diario', 'Evento'];
  }

  if (slug === 'cadenas') {
    if (name.includes('brazalete')) return ['Evento', 'Uso diario'];
    if (name.includes('delicada') || name.includes('bolitas')) return ['Uso diario', 'Regalo especial'];
    return ['Layering', 'Uso diario'];
  }

  if (name.includes('media eternidad') || name.includes('pave')) return ['Evento', 'Regalo especial'];
  if (name.includes('esferas')) return ['Uso diario', 'Regalo especial'];
  return ['Uso diario', 'Evento'];
}

function inferMaterial(slug, item) {
  if (slug === 'anillos') {
    if (item.category === 'Oro Blanco') return 'Oro blanco con brillo espejo';
    if (item.category === 'Bicolor') return 'Combinacion de oro amarillo y blanco';
    return 'Oro amarillo con pulido fino';
  }

  if (slug === 'cadenas') {
    if (item.name.includes('Brazalete')) return 'Oro amarillo con estructura rigida';
    if (item.name.includes('Bolitas')) return 'Oro amarillo con esferas pulidas';
    return 'Oro amarillo trabajado en eslabones artesanales';
  }

  if (slug === 'pulseras') {
    if (item.name.includes('Pave') || item.name.includes('Eternidad')) return 'Oro amarillo con acentos brillantes';
    if (item.name.includes('Rigido')) return 'Oro amarillo en formato brazalete';
    return 'Oro amarillo con textura artesanal';
  }

  if (item.name.includes('Perla')) return 'Oro amarillo con perla protagonista';
  if (item.name.includes('Pave') || item.name.includes('Brillo') || item.name.includes('Topos')) return 'Oro amarillo con detalle brillante';
  return 'Oro amarillo con caida ligera';
}

function inferFinish(slug, itemName) {
  const name = itemName.toLowerCase();

  if (name.includes('pave') || name.includes('brillo') || name.includes('eternidad')) return 'Pulido alto con detalle de brillo';
  if (name.includes('perla')) return 'Brillo suave con protagonismo central';
  if (name.includes('trenzad') || name.includes('cordon')) return 'Textura trenzada y terminacion pulida';
  if (slug === 'cadenas') return 'Perfil limpio con caida ligera';
  if (slug === 'pulseras') return 'Pulido espejo y presencia marcada';
  return 'Pulido fino de alta presencia';
}

function inferIdealFor(slug, type, occasions, protagonism) {
  if (slug === 'anillos') {
    if (type === 'Solitario' || type === 'Halo') return 'Compromiso, aniversario o una declaracion con mucha carga simbolica.';
    if (type.includes('Eternidad')) return 'Aniversarios, regalos con permanencia o stacking refinado.';
    return 'Regalo especial o una pieza de simbolo personal con lectura elegante.';
  }

  if (slug === 'cadenas') {
    return protagonism === 'Bajo'
      ? 'Uso diario elevado, layering delicado o regalo versatil.'
      : 'Capas mas visibles, looks de presencia o eventos con una lectura mas marcada.';
  }

  if (slug === 'pulseras') {
    return 'Pulso con caracter para evento, regalo especial o uso continuo con mas presencia.';
  }

  return occasions.includes('Uso diario')
    ? 'Uso diario con un acento refinado o regalo facil de acertar.'
    : 'Eventos, regalo especial o una pieza para iluminar mas el gesto.';
}

function inferCombinesWith(slug, type, style) {
  if (slug === 'anillos') {
    if (type === 'Solitario') return 'Se complementa bien con bandas finas o medias eternidades de lectura sobria.';
    if (type.includes('Eternidad')) return 'Funciona muy bien junto a solitarios y composiciones de stacking delicado.';
    return 'Conviene combinarlo con aretes ligeros o una cadena limpia para no competir en exceso.';
  }

  if (slug === 'aretes') {
    if (style === 'Delicado' || style === 'Ligero') return 'Combinan muy bien con cadenas finas o anillos sobrios.';
    return 'Se ven mejor con una cadena simple o una pulsera limpia que deje respirar la pieza.';
  }

  if (slug === 'cadenas') {
    return 'Se integran bien con aretes pequenos, anillos clasicos o una pulsera de pulido limpio.';
  }

  return 'Puedes elevarla con aretes discretos o una cadena ligera, segun la ocasion.';
}

function buildSummary(collectionTitle, itemName, idealFor) {
  return `${itemName} pertenece a la coleccion ${collectionTitle.toLowerCase()} y mantiene el lenguaje elegante de El Atelier Artesanal, con materiales nobles, proporciones limpias y una presencia refinada. ${idealFor}`;
}

function buildConfiguratorPrompt(collectionTitle, itemName) {
  return `Quiero una propuesta inspirada en ${itemName.toLowerCase()} de la coleccion ${collectionTitle.toLowerCase()}, con un lenguaje elegante y artesanal. Me interesa explorar variaciones de material, proporciones y acabados sin perder la esencia de una joya fina hecha a mano.`;
}

function buildQuoteMessage(collectionTitle, metadata) {
  return [
    'Hola, quiero solicitar una cotizacion.',
    `Coleccion: ${collectionTitle}`,
    `Pieza: ${metadata.name}`,
    `Referencia: ${metadata.reference}`,
    `Material sugerido: ${metadata.material}`,
    `Acabado: ${metadata.finish}`,
    `Ideal para: ${metadata.idealForShort}`,
    'Quisiera conocer opciones de precio, materiales y tiempo estimado de elaboracion.',
  ].join('\n');
}

function enrichItem(collectionSlug, collection, item) {
  const type = inferType(collectionSlug, item.name);
  const occasions = inferOccasions(collectionSlug, item.name);
  const style = inferStyle(collectionSlug, item.name);
  const protagonism = inferProtagonism(collectionSlug, item.name);
  const material = inferMaterial(collectionSlug, item);
  const finish = inferFinish(collectionSlug, item.name);
  const metal = inferMetal(collectionSlug, item);
  const idealFor = inferIdealFor(collectionSlug, type, occasions, protagonism);
  const combinesWith = inferCombinesWith(collectionSlug, type, style);
  const reference = createReference(collectionSlug, item.id);
  const summary = buildSummary(collection.title, item.name, idealFor);

  return {
    ...item,
    reference,
    metal,
    material,
    finish,
    type,
    style,
    protagonism,
    occasions,
    idealFor,
    idealForShort: occasions.join(', '),
    combinesWith,
    summary,
    prompt: buildConfiguratorPrompt(collection.title, item.name),
  };
}

function unique(values) {
  return ['Todos', ...Array.from(new Set(values.filter(Boolean)))];
}

export function getCollectionCatalog(slug) {
  const collection = rawCollectionConfig[slug];

  if (!collection) {
    return null;
  }

  const items = collection.items.map((item) => enrichItem(slug, collection, item));

  return {
    ...collection,
    slug,
    items,
  };
}

export function getCollectionFilterOptions(collection) {
  if (!collection) {
    return {
      metal: ['Todos'],
      type: ['Todos'],
      occasion: ['Todos'],
      style: ['Todos'],
      protagonism: ['Todos'],
    };
  }

  return {
    metal: unique(collection.items.map((item) => item.metal)),
    type: unique(collection.items.map((item) => item.type)),
    occasion: unique(collection.items.flatMap((item) => item.occasions)),
    style: unique(collection.items.map((item) => item.style)),
    protagonism: unique(collection.items.map((item) => item.protagonism)),
  };
}

export function filterCollectionItems(items, filters) {
  return items.filter((item) => {
    if (filters.metal !== 'Todos' && item.metal !== filters.metal) return false;
    if (filters.type !== 'Todos' && item.type !== filters.type) return false;
    if (filters.occasion !== 'Todos' && !item.occasions.includes(filters.occasion)) return false;
    if (filters.style !== 'Todos' && item.style !== filters.style) return false;
    if (filters.protagonism !== 'Todos' && item.protagonism !== filters.protagonism) return false;
    return true;
  });
}

export function getRelatedItems(collection, currentItem) {
  if (!collection || !currentItem) {
    return [];
  }

  const sameType = [];
  const sameStyle = [];
  const sameOccasion = [];

  collection.items.forEach((item) => {
    if (item.id === currentItem.id) {
      return;
    }

    if (item.type === currentItem.type) {
      sameType.push(item);
      return;
    }

    if (item.style === currentItem.style || item.protagonism === currentItem.protagonism) {
      sameStyle.push(item);
      return;
    }

    if (item.occasions.some((occasion) => currentItem.occasions.includes(occasion))) {
      sameOccasion.push(item);
    }
  });

  return [...sameType, ...sameStyle, ...sameOccasion].slice(0, 3);
}

export function buildProductQuoteMessage(collection, item) {
  return buildQuoteMessage(collection.title, item);
}

function getTopEntries(values, limit = 2) {
  const counts = new Map();

  values.forEach((value) => {
    if (!value) {
      return;
    }

    counts.set(value, (counts.get(value) || 0) + 1);
  });

  return [...counts.entries()]
    .sort((left, right) => right[1] - left[1])
    .slice(0, limit)
    .map(([value]) => value);
}

export function getCollectionInsights(collection) {
  if (!collection) {
    return {
      itemCount: 0,
      topOccasions: [],
      topStyles: [],
      topTypes: [],
      editorialLine: '',
    };
  }

  const topOccasions = getTopEntries(collection.items.flatMap((item) => item.occasions), 2);
  const topStyles = getTopEntries(collection.items.map((item) => item.style), 2);
  const topTypes = getTopEntries(collection.items.map((item) => item.type), 2);

  return {
    itemCount: collection.items.length,
    topOccasions,
    topStyles,
    topTypes,
    editorialLine: `${collection.items.length} referencias con lectura ${topStyles.join(' y ').toLowerCase()} para ${topOccasions.join(' y ').toLowerCase()}.`,
  };
}

